<?php

    // Even and Odd Number Program using For Loop Developed by Sandesh Marathe

    // iteration of for loop from 1 to 10
    for ($i = 1; $i <= 10; $i++) {

        // condition to get even number
        if ($i % 2 == 0){

            echo "\n Even Number : ".$i;
        } else {

            echo "\n Odd Number : ".$i;
        }
    }

?>